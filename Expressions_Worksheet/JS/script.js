// Slice of Pie Part I //
var numberSlices = 8;
var numberPeople = 67;
var numberPizzas = 18;
var totalSlices = numberSlices * numberPizzas;
var slicesPerPerson = totalSlices / numberPeople;
console.log("Each person ate " + slicesPerPerson + " slices of pizza at the party.");
// Slices of Pie Part II //
var slicesEaten = totalSlices - slicesPerPerson;
var sparkySlices = totalSlices - slicesEaten;
console.log("Sparky got " + sparkySlices + " slices of pizza.");
